/** 单幕页内容（人物剧本） */
export interface CharacterPage {
  /** 幕次标签，如 "第一幕" */
  act: string;
  /** 幕标题，如 "归乡" */
  title: string;
  /** 副标题，如 "案前夜 · 雨落沈宅" */
  subtitle: string;
  /** 段落列表，可能含 <span class="highlight">…</span> */
  paragraphs: string[];
}

/** 人物剧本节点 */
export interface CharacterNode {
  type: 'character';
  /** 节点 ID，如 "char-shenmobai" */
  id: string;
  name: string;
  /** 角色身份，如 "死者视角 · 沈家家主" */
  role: string;
  /** 人物配色 hex */
  color: string;
  pages: CharacterPage[];
}

/** 简单内容节点（组织者手册 / 真相复盘） */
export interface SimpleNode {
  type: 'simple';
  id: string;
  title: string;
  /** 幕次标签，如 "第二幕" / "全本" / "终局" */
  actNum: string;
  /** 完整标题，用于工具栏展示 */
  fullTitle: string;
  /** 预渲染 HTML 内容 */
  html: string;
}

/** 线索卡 */
export interface ScriptClue {
  /** 编号，如 "#001" */
  no: string;
  title: string;
  /** 标签文本，如 "关键" / "物证" / "口供" / "环境" / "情感" / "机制" / "伪线索" */
  tag: string;
  /** 标签配色：blood（关键）/ ok（伪线索）/ undefined（默认） */
  tagType?: 'blood' | 'ok';
  /** 搜证地点 */
  loc: string;
}

/** 线索卡总览节点 */
export interface ClueOverviewNode {
  type: 'clue-overview';
  id: string;
  title: string;
  actNum: string;
  fullTitle: string;
  clues: ScriptClue[];
}

/** 编辑器节点联合类型 */
export type ScriptNodeData = CharacterNode | SimpleNode | ClueOverviewNode;

/** 章节树分组定义 */
export interface TreeGroup {
  /** 分组 ID */
  group: string;
  /** 分组标题 */
  label: string;
  /** 子节点 ID 列表 */
  children: string[];
  /** Optional display count override. */
  count?: number;
}

